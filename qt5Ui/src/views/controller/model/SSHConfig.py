class SSHConfig():
    def __init__(self):
        self.ip = None
        self.port = None
        self.username = None
        self.password = None
    def setIP(ip):
        self.ip = ip
    def setPort(port):
        self.port = port
    def setUsername(username):
        self.username = username
    def setPassword(password):
        self.password = password
    def getIP():
        return self.ip
    def getPort():
        return self.port
    def getUsername():
        return self.username
    def getPassword():
        return self.password